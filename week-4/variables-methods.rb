# How do you define a local variable?

A local variable is local in scope and can only be accessed in certain places. For example, if a variable is defined in a method, that variable defined outside of the method can have a different value assigned to it although both variables can be named the same.

How do you define a method?

Methods are blocks of code the computer executes instead of having to write code over and over.

What is the difference between a local variable and a method?

Variables store information assigned to them. Meanwhile methods allow us to execute a piece of code repeadtedly.

